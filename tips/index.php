                <h3>Impresi&oacute;n de fotograf&iacute;as</h3>
                <p>Para que tu fotograf&iacute;a tenga la mejor calidad de impresi&oacute;n posible, la imagen debe ser de un tama&ntilde;o no menor al de la hoja en la cual se va a imprimir; por ejemplo, si la impresi&oacute;n va a ser en una hoja de 44 x 30 pulgadas (111x76cm), la imagen deber&iacute;a tener preferentemente 3300 x 2250 p&iacute;xeles (a 75 ppi de resoluci&oacute;n) como m&iacute;nimo. Si tu fotograf&iacute;a tiene m&aacute;s p&iacute;xeles, el tamaño de impresi&oacute;n puede ser mayor.</p>
                <p>Para imprimir fotograf&iacute;as e im&aacute;genes, nosotros usamos rollos de papel fotogr&aacute;fico de 44 y 24 pulgadas de ancho y hojas de papel fotogr&aacute;fico tama&ntilde;o tama&ntilde;o A4. El largo puede variar dependiendo del requirimiento del cliente si se imprime en rollo de papel.</p>                
                <p>Puedes traer tus im&aacute;genes en formato Raw (de diferentes fabricantes de c&aacute;mara), TIFF, JPEG, PSD e incluso PNG o GIF; si tienen la calidad necesaria, podemos imprimir desde esos formatos. No es necesario convertir tus im&aacute;genes de RGB a CMYK.</p>
                

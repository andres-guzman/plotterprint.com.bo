              <img alt="Tips Diseño Gráfico" class="help-img" src="../img/tips_design.jpg" />
              <h3>Dise&ntilde; gr&aacute;fico</h3>
              <h3>Photoshop</h3>
              <p>Si tienes un dise&ntilde;o en Photoshop puedes traer tu archivo PSD para que imprimamos directamente, sin embargo, es conveniente que guardes tu diseño a alg&uacute;n formato de imagen (TIFF, BMP, JPEG, etc). Con Photoshop puedes guardar tu archivo en varios formatos; nosotros te recomendamos que guardes tus diseños en formato TIFF sin capas y con compresi&oacute;n LWZ. Esto no reduce la calidad de la imagen pero reduce el tamaño del archivo.</p> 
              <h3>InDesign:</h3>
              <p>Podemos imprimir desde tus archivos de InDesign (indd). Esto es conveniente por que no hace falta que exportes a PDF. Antes de traer un trabajo no te olvides empaquetar tus arhchivos para que se incluyan las fuentes y los archivos necesarios que est&aacute;n en tu dise&ntilde;o. M&aacute;s ayuda sobre como empaquetar <a href="https://helpx.adobe.com/es/indesign/using/preflighting-files-handoff.html#package_files">aqu&iacute;.</a></p>
              <p>Si ya tienes un dise&ntilde;o listo en PDF puedes traerlo de igual manera. Para aprender como exportar un arhivo de InDesign a PDF puedes hacer clic <a href="https://helpx.adobe.com/la/indesign/using/exporting-publishing-pdf.html">aqu&iacute;.</a></p>
              <h3>Illustrator</h3>
              <p>Podemos imprimir desde tus archivos creados con Adobe Illustrator, pero antes debes empaquetar los archivos y fuentes que usaste para crear tu diseño. <a href="https://helpx.adobe.com/es/illustrator/using/package-files.html">Aqu&iacute;</a> puedes ver como se empaqueta archivos con Adobe Illustartor.</p>
              <p>Tambi&eacute;n existe la opci&oacute;n de guardar ilustraciones en PDF (las fuentes y los archivos se incluyen en el documento PDF). Para ver como guardar en este formato, puedes hacer click <a href="https://helpx.adobe.com/es/illustrator/using/creating-pdf-files.html">aqu&iacute;.</a></p>
              <p>Si exportaste una ilustraci&oacute;n a un formato de mapa de bits, podemos imprimirlo tambi&eacute;n, pero por favor toma en cuenta los consejos que mencionamos para fotograf&iacute;as antes en esta p&aacute;gina.</p>
